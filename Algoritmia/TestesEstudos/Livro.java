class Livro
{
    String titulo;
    String autor;
    int anoPublicacao;
    
    Livro(){
        
    }
    
    public Livro(String paramTitulo, String paramAutor, int paramAnoPublicacao)
    {
        titulo = paramTitulo;
        autor = paramAutor;
        anoPublicacao = paramAnoPublicacao;
    }
}
